package level2;

public class notepad {

	public static void main(String[] args) {
		String s = "";
		System.out.println(s.length());
		System.out.println(s.equals(""));
		s += "hello";
		System.out.println(s);
		
		s = "";
		System.out.println(s);
		s += "hi";
		System.out.println(s);
		
		System.out.println("'(None)'");

	}

}
